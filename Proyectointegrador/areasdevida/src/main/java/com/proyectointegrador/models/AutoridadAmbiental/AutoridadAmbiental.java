public class AutoridadAmbiental {
    private int id;
    private String nombreAutoridad;
    private String tipoAutoridad;
    private String contacto;
    private String jurisdiccion;

    // Getters y Setters
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombreAutoridad() {
        return nombreAutoridad;
    }

    public void setNombreAutoridad(String nombreAutoridad) {
        this.nombreAutoridad = nombreAutoridad;
    }

    public String getTipoAutoridad() {
        return tipoAutoridad;
    }

    public void setTipoAutoridad(String tipoAutoridad) {
        this.tipoAutoridad = tipoAutoridad;
    }

    public String getContacto() {
        return contacto;
    }

    public void setContacto(String contacto) {
        this.contacto = contacto;
    }

    public String getJurisdiccion() {
        return jurisdiccion;
    }

    public void setJurisdiccion(String jurisdiccion) {
        this.jurisdiccion = jurisdiccion;
    }
}
